namespace Math
{

/**
 * @brief   Computes the largest float value not greater than value depending on an interval
 * @param   value on which the floor will be applied
 * @param   interval
 */
float floor(float value, float interval);

/**
 * @brief   Computes the smallest float value not less than value depending on an interval
 * @param   value on which the ceil will be applied
 * @param   interval
 */
float ceil(float value, float interval);

}
